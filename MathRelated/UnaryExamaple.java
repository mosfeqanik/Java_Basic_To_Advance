package basic.MathRelated;

public class UnaryExamaple {

    public static void main(String[] args) {
        int x=10;
        int result=12;
        
        System.out.println("Result :"+ result);
        result = result + x;//result =10+12=22
        System.out.println("Result :"+ result);
        result=+x;//result = + 10
        System.out.println("Result :"+ result);
        result=-x;//result = - 10
        System.out.println("Result :"+ result);
        
        
    }
    
}
