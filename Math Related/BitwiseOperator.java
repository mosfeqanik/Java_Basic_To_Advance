
package basic;

public class BitwiseOperator {
    public static void main(String[] args) {
        int a = 32;
        int b = 12;
        
        int c;
        
        c = a&b;    
        //and e Multiplication hoi bit wise
        System.out.println("a & b :" +c);
         
        c = a|b;    
        //OR e Plus hoi bit wise
        System.out.println("a | b :" +c);
        
        c = a^b;    
        //EXOR e akta one thakle  result e one  hoi bit wise
        System.out.println("a ^ b :" +c);
        
    
        
    }
 
}
