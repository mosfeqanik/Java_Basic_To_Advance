public class DowhileLoopUsage {
    public static void main(String[] args) {
        
        int i=1;
        do{
            System.out.println(i+".Bangladesh");
            i++;
        }while(i<=5);
        
        i =2;
        do{
            System.out.println(i+"Even number");
            i=i+2;
        }while(i <=100);
        
        i =100;
        do{
            System.out.println(i+"Even number");
            i=i-2;
        }while(i >=2);
        
      
        
        
    }
}
