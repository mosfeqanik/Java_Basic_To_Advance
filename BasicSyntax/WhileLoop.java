package basic.BasicSyntax;

public class WhileLoop {
    public static void main(String[] args) {
        int i = 1;
        while(i<=5){
            System.out.println(i+"."+"Bangladesh");
            i++;
        }
        i =2;
        while(i<=100){
            System.out.println(i+"Even number");
            i=i+2;
        }
        i =100;
        while(i>=2){
            System.out.println(i+"Even number");
            i=i-2;
        }
    }
}
